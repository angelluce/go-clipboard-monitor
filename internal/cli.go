func (cli *CLI) Run() {
	cli.PrintWelcome()

	scanner := bufio.NewScanner(os.Stdin)
	prompt := "> "

	for {
		fmt.Print(prompt)

		if !scanner.Scan() {
			break
		}

		input := strings.TrimSpace(scanner.Text())
		if input == "" {
			continue
		}

		cli.processCommand(input)
	}
}

